# Модуль math в MicroPython предоставляет следующие функции:

# Тригонометрические функции:
# math.acos(x) - Арккосинус числа x (в радианах)
# math.acosh(x) - Обратный гиперболический косинус числа x
# math.asin(x) - Арксинус числа x (в радианах)
# math.asinh(x) - Обратный гиперболический синус числа x
# math.atan(x) - Арктангенс числа x (в радианах)
# math.atan2(y, x) - Арктангенс от двух переменных y и x (в радианах)
# math.atanh(x) - Обратный гиперболический тангенс числа x

# Округление и целые числа:
# math.ceil(x) - Округление числа x вверх до ближайшего целого
# math.floor(x) - Округление числа x вниз до ближайшего целого
# math.trunc(x) - Обрезает дробную часть числа x (целая часть)

# Математические операции:
# math.copysign(x, y) - Возвращает число x с знаком, равным знаку числа y
# math.fabs(x) - Абсолютное значение числа x
# math.factorial(x) - Факториал числа x
# math.fmod(x, y) - Остаток от деления x на y (x % y)
# math.frexp(x) - Разлагает число x на мантиссу и степень 2 (возвращает кортеж)
# math.ldexp(x, exp) - Возводит 2 в степень exp и умножает на x
# math.modf(x) - Разделяет число x на дробную и целую часть
# math.sqrt(x) - Квадратный корень из числа x

# Логарифмические функции:
# math.log(x) - Натуральный логарифм числа x
# math.log10(x) - Логарифм по основанию 10 числа x
# math.log2(x) - Логарифм по основанию 2 числа x

# Гиперболические функции:
# math.cos(x) - Косинус числа x (в радианах)
# math.cosh(x) - Гиперболический косинус числа x
# math.sin(x) - Синус числа x (в радианах)
# math.sinh(x) - Гиперболический синус числа x
# math.tan(x) - Тангенс числа x (в радианах)
# math.tanh(x) - Гиперболический тангенс числа x

# Прочие функции:
# math.degrees(x) - Переводит угол из радиан в градусы
# math.radians(x) - Переводит угол из градусов в радианы
# math.gamma(x) - Функция гамма от числа x
# math.lgamma(x) - Логарифм гамма-функции
# math.erf(x) - Ошибочная функция от числа x
# math.erfc(x) - Дополнительная ошибка от числа x
# math.expm1(x) - Возводит e в степень x и вычитает 1 (экспоненциальная точность)

# Константы:
# math.e - Основание натурального логарифма (примерно 2.71828)
# math.pi - Число Пи (примерно 3.14159)
# math.tau - Тау (примерно 6.28318, двойное число Пи)
# math.inf - Представление бесконечности
# math.nan - Не число (Not a Number)

# Функции для проверки:
# math.isclose(a, b) - Проверяет, близки ли два числа a и b с учётом погрешности
# math.isfinite(x) - Проверяет, является ли число x конечным
# math.isinf(x) - Проверяет, является ли число x бесконечностью
# math.isnan(x) - Проверяет, является ли число x значением NaN (не число)

