# Ввод чисел с клавиатуры
a = int(input("Введите первое число (a): "))
b = int(input("Введите второе число (b): "))

# Сложение
sum_ab = a + b

# Вычитание
diff_ab = a - b

# Умножение
mult_ab = a * b

# Деление
div_ab = a / b

# Целочисленное деление
floor_div_ab = a // b

# Остаток от деления (модуль)
mod_ab = a % b

# Возведение в степень
pow_ab = a ** b

# Абсолютное значение
abs_ab = abs(a)

# Округление числа
rounded_ab = round(a)

# Целая часть (округление вниз)
floor_value = int(a)

# Квадратный корень
sqrt_ab = a ** 0.5

# Вывод результатов
print("\nРезультаты:")
print("Сложение:", sum_ab)
print("Вычитание:", diff_ab)
print("Умножение:", mult_ab)
print("Деление:", div_ab)
print("Целочисленное деление:", floor_div_ab)
print("Остаток от деления:", mod_ab)
print("Возведение в степень:", pow_ab)
print("Абсолютное значение:", abs_ab)
print("Округление:", rounded_ab)
print("Целая часть:", floor_value)
print("Квадратный корень:", sqrt_ab)

